export const accountClassEnum = pgEnum("account_class", [
  "eiendeler",       // 1000-1999
  "egenkapital",     // 2000-2099
  "gjeld",           // 2100-2999
  "inntekter",       // 3000-3999
  "varekostnad",     // 4000-4999
  "lonnskostnad",    // 5000-5999
  "avskrivninger",   // 6000-6999
  "andre_kostnader", // 7000-7999
  "finans",          // 8000-8999
]);

export const voucherStatusEnum = pgEnum("voucher_status", [
  "utkast",        // AI har foreslått kontering, RF har ikke sett
  "foreslatt",     // AI-foreslått, venter på RF
  "godkjent",      // RF har godkjent posteringen
  "postert",       // Bokført i hovedbok
  "avvist",        // RF avviste AI-forslaget
]);

export const bankLineStatusEnum = pgEnum("bank_line_status", [
  "uavstemt",      // Ikke koblet til noen postering
  "avstemt",       // Koblet til bilagspostering
  "ignorert",      // Manuelt markert som ikke relevant
]);

// Kontoplan — NS 4102 standardkontoer + selskapsspesifikke
export const accounts = pgTable("accounts", {
  id: uuid("id").defaultRandom().primaryKey(),
  companyId: uuid("company_id")
    .references(() => companies.id)
    .notNull(),
  accountNumber: integer("account_number").notNull(), // 1000-8999
  name: text("name").notNull(),
  accountClass: accountClassEnum("account_class").notNull(),
  isStandard: boolean("is_standard").notNull().default(true), // NS 4102 standard
  active: boolean("active").notNull().default(true),
  createdAt: timestamp("created_at").defaultNow().notNull(),
}, (table) => [
  index("accounts_company_number_idx").on(table.companyId, table.accountNumber),
]);

// Bilag (vouchers) — samler posteringene for én transaksjon
export const vouchers = pgTable("vouchers", {
  id: uuid("id").defaultRandom().primaryKey(),
  companyId: uuid("company_id")
    .references(() => companies.id)
    .notNull(),
  cycleId: uuid("cycle_id")
    .references(() => cycles.id)
    .notNull(),
  voucherNumber: integer("voucher_number").notNull(), // Løpende bilagsnummer per år
  voucherDate: date("voucher_date").notNull(),        // Transaksjonsdato
  description: text("description").notNull(),
  documentId: uuid("document_id")                      // Lenke til opplastet fil
    .references(() => documents.id),
  status: voucherStatusEnum("status").notNull().default("utkast"),
  aiConfidence: numeric("ai_confidence", { precision: 3, scale: 2 }), // 0.00-1.00
  aiReasoning: text("ai_reasoning"),                   // AI sin begrunnelse for kontering
  createdBy: uuid("created_by")
    .references(() => users.id)
    .notNull(),
  approvedBy: uuid("approved_by")
    .references(() => users.id),
  approvedAt: timestamp("approved_at"),
  createdAt: timestamp("created_at").defaultNow().notNull(),
}, (table) => [
  index("vouchers_company_cycle_idx").on(table.companyId, table.cycleId),
]);

// Posteringer (journal entries) — debet/kredit-linjer på bilag
export const journalEntries = pgTable("journal_entries", {
  id: uuid("id").defaultRandom().primaryKey(),
  voucherId: uuid("voucher_id")
    .references(() => vouchers.id)
    .notNull(),
  accountId: uuid("account_id")
    .references(() => accounts.id)
    .notNull(),
  debit: numeric("debit", { precision: 14, scale: 2 }).notNull().default("0"),
  credit: numeric("credit", { precision: 14, scale: 2 }).notNull().default("0"),
  description: text("description"), // Valgfri linjetekst
}, (table) => [
  index("journal_entries_voucher_idx").on(table.voucherId),
  index("journal_entries_account_idx").on(table.accountId),
]);

// Åpningsbalanse — IB per konto per syklus
export const openingBalances = pgTable("opening_balances", {
  id: uuid("id").defaultRandom().primaryKey(),
  companyId: uuid("company_id")
    .references(() => companies.id)
    .notNull(),
  cycleId: uuid("cycle_id")
    .references(() => cycles.id)
    .notNull(),
  accountId: uuid("account_id")
    .references(() => accounts.id)
    .notNull(),
  balance: numeric("balance", { precision: 14, scale: 2 }).notNull(), // Positiv = debet, negativ = kredit
  createdAt: timestamp("created_at").defaultNow().notNull(),
}, (table) => [
  index("opening_balances_cycle_idx").on(table.companyId, table.cycleId),
]);

// Banklinjer — importerte transaksjoner fra bankutskrift
export const bankLines = pgTable("bank_lines", {
  id: uuid("id").defaultRandom().primaryKey(),
  companyId: uuid("company_id")
    .references(() => companies.id)
    .notNull(),
  cycleId: uuid("cycle_id")
    .references(() => cycles.id)
    .notNull(),
  documentId: uuid("document_id")                      // Bankutskrift-dokumentet
    .references(() => documents.id),
  transactionDate: date("transaction_date").notNull(),
  description: text("description").notNull(),
  amount: numeric("amount", { precision: 14, scale: 2 }).notNull(), // Positiv = inn, negativ = ut
  bankReference: text("bank_reference"),               // Referanse fra banken
  status: bankLineStatusEnum("status").notNull().default("uavstemt"),
  matchedVoucherId: uuid("matched_voucher_id")         // Koblet bilag ved avstemming
    .references(() => vouchers.id),
  createdAt: timestamp("created_at").defaultNow().notNull(),
}, (table) => [
  index("bank_lines_company_cycle_idx").on(table.companyId, table.cycleId),
]);
