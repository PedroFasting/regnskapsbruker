import { pgTable, text, boolean, timestamp, integer, pgEnum, uuid, numeric, date, index } from "drizzle-orm/pg-core";

// Enums
export const userRoleEnum = pgEnum("user_role", ["rf", "kunde", "admin"]);
export const companyTypeEnum = pgEnum("company_type", ["AS", "ENK", "NUF"]);
export const processModelEnum = pgEnum("process_model", ["aarsbasert", "loepende"]);
export const taskTypeEnum = pgEnum("task_type", [
  "dokument_opplasting",
  "bekreftelse",
  "godkjenning",
  "signering",
]);
export const taskStatusEnum = pgEnum("task_status", [
  "ikke_startet",
  "levert",
  "godkjent",
  "trenger_mer",
]);
export const cycleStatusEnum = pgEnum("cycle_status", [
  "aktiv",
  "fullfort",
  "kansellert",
]);
export const documentStatusEnum = pgEnum("document_status", [
  "lastet_opp",
  "godkjent_av_rf",
  "avvist",
]);
export const documentCategoryEnum = pgEnum("document_category", [
  "bilag",
  "bankutskrift",
  "aapningsbalanse",
  "annet",
]);
export const approvalTypeEnum = pgEnum("approval_type", [
  "aarsregnskap",
  "generalforsamling",
]);
export const contactRoleEnum = pgEnum("contact_role", [
  "daglig_leder",
  "styreleder",
  "okonomiansvarlig",
  "annet",
]);

// Users
export const users = pgTable("users", {
  id: uuid("id").defaultRandom().primaryKey(),
  email: text("email").notNull().unique(),
  name: text("name").notNull(),
  passwordHash: text("password_hash"),
  role: userRoleEnum("role").notNull().default("kunde"),
  createdAt: timestamp("created_at").defaultNow().notNull(),
});

// Accountants
export const accountants = pgTable("accountants", {
  id: uuid("id").defaultRandom().primaryKey(),
  userId: uuid("user_id")
    .references(() => users.id)
    .notNull()
    .unique(),
  firmName: text("firm_name"),
  createdAt: timestamp("created_at").defaultNow().notNull(),
});

// Companies
export const companies = pgTable("companies", {
  id: uuid("id").defaultRandom().primaryKey(),
  orgNr: text("org_nr").notNull().unique(),
  name: text("name").notNull(),
  companyType: companyTypeEnum("company_type").notNull().default("AS"),
  vatRegistered: boolean("vat_registered").notNull().default(false),
  hasEmployees: boolean("has_employees").notNull().default(false),
  processModel: processModelEnum("process_model")
    .notNull()
    .default("aarsbasert"),
  address: text("address"),
  industryCode: text("industry_code"),
  foundedDate: text("founded_date"),
  accountantId: uuid("accountant_id")
    .references(() => accountants.id)
    .notNull(),
  createdAt: timestamp("created_at").defaultNow().notNull(),
});

// Contacts (customer users linked to companies)
export const contacts = pgTable("contacts", {
  id: uuid("id").defaultRandom().primaryKey(),
  companyId: uuid("company_id")
    .references(() => companies.id)
    .notNull(),
  userId: uuid("user_id").references(() => users.id),
  name: text("name").notNull(),
  email: text("email").notNull(),
  role: contactRoleEnum("role").notNull().default("daglig_leder"),
  createdAt: timestamp("created_at").defaultNow().notNull(),
});

// Cycles (accounting year)
export const cycles = pgTable("cycles", {
  id: uuid("id").defaultRandom().primaryKey(),
  companyId: uuid("company_id")
    .references(() => companies.id)
    .notNull(),
  year: integer("year").notNull(),
  status: cycleStatusEnum("status").notNull().default("aktiv"),
  createdAt: timestamp("created_at").defaultNow().notNull(),
  completedAt: timestamp("completed_at"),
});

// Tasks (generated by process engine)
export const tasks = pgTable("tasks", {
  id: uuid("id").defaultRandom().primaryKey(),
  cycleId: uuid("cycle_id")
    .references(() => cycles.id)
    .notNull(),
  title: text("title").notNull(),
  description: text("description").notNull(),
  helpText: text("help_text"),
  type: taskTypeEnum("type").notNull(),
  sortOrder: integer("sort_order").notNull(),
  required: boolean("required").notNull().default(true),
  status: taskStatusEnum("status").notNull().default("ikke_startet"),
  deadlineRelative: text("deadline_relative"),
  deliveredAt: timestamp("delivered_at"),
  approvedAt: timestamp("approved_at"),
  rfComment: text("rf_comment"),
});

// Documents
export const documents = pgTable("documents", {
  id: uuid("id").defaultRandom().primaryKey(),
  taskId: uuid("task_id")
    .references(() => tasks.id),                        // Nullable: null for RF-opplastede dokumenter
  companyId: uuid("company_id")
    .references(() => companies.id)
    .notNull(),
  cycleId: uuid("cycle_id")
    .references(() => cycles.id)
    .notNull(),
  category: documentCategoryEnum("category").notNull().default("bilag"),
  originalFilename: text("original_filename").notNull(),
  storedPath: text("stored_path").notNull(),
  fileType: text("file_type").notNull(),
  fileSize: integer("file_size").notNull(),
  uploadedBy: uuid("uploaded_by")
    .references(() => users.id)
    .notNull(),
  uploadedAt: timestamp("uploaded_at").defaultNow().notNull(),
  status: documentStatusEnum("status").notNull().default("lastet_opp"),
  rejectionReason: text("rejection_reason"),
});

// Approvals
export const approvals = pgTable("approvals", {
  id: uuid("id").defaultRandom().primaryKey(),
  companyId: uuid("company_id")
    .references(() => companies.id)
    .notNull(),
  cycleId: uuid("cycle_id")
    .references(() => cycles.id)
    .notNull(),
  documentId: uuid("document_id").references(() => documents.id),
  type: approvalTypeEnum("type").notNull(),
  approvedBy: uuid("approved_by")
    .references(() => users.id)
    .notNull(),
  approvedAt: timestamp("approved_at").defaultNow().notNull(),
  ipAddress: text("ip_address"),
  userAgent: text("user_agent"),
});

// Notifications
export const notifications = pgTable("notifications", {
  id: uuid("id").defaultRandom().primaryKey(),
  recipientUserId: uuid("recipient_user_id")
    .references(() => users.id)
    .notNull(),
  recipientEmail: text("recipient_email").notNull(),
  type: text("type").notNull(),
  companyId: uuid("company_id").references(() => companies.id),
  content: text("content").notNull(),
  sentAt: timestamp("sent_at"),
  readAt: timestamp("read_at"),
  channel: text("channel").notNull().default("epost"),
  relatedTaskId: uuid("related_task_id").references(() => tasks.id),
});

// Invitations
export const invitations = pgTable("invitations", {
  id: uuid("id").defaultRandom().primaryKey(),
  companyId: uuid("company_id")
    .references(() => companies.id)
    .notNull(),
  email: text("email").notNull(),
  token: text("token").notNull().unique(),
  expiresAt: timestamp("expires_at").notNull(),
  acceptedAt: timestamp("accepted_at"),
  createdAt: timestamp("created_at").defaultNow().notNull(),
});

